#!/usr/bin/env scala

val string = "abcde"

println("string: " + string)

println("take(2): " + string.take(2))
println("drop(3): " + string.drop(3))
println("takeRight(2): " + string.takeRight(2))
println("dropRight(3): " + string.dropRight(3))
println("substring(2, 4): " + string.substring(2, 4))

